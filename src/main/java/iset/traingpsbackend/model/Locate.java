package iset.traingpsbackend.model;

public class Locate {
    private double lat;
    private double lon;

    public Locate() {
    }


    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLon() {
        return lon;
    }

    public void setLon(double lon) {
        this.lon = lon;
    }
}
